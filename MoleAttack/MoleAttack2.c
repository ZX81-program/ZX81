unsigned char *scrnlinep[D_DY];
#define _countof(array)    (sizeof(array) / sizeof(array[0]))

void disphole(int x, int y, int chrno)
{
    unsigned char *scrnp, *chrp;
    int  i;

    scrnp = scrnlinep[y] + x;
    chrp = &chrtbl[chrno][0];
    for (i = 0; i < CHR_DY; i++) {
        if (chrp[0] != 0xff) {
            memcpy(scrnp, chrp, CHR_DX);
        }
        scrnp += D_DX;
        chrp += CHR_DX;
    }
}

int main()
{
    int  i, x, y;

    for (i = 0; i < _countof(scrnlinep); i++) {
        scrnlinep[i] = (unsigned char *)((*((unsigned int *)D_FILE) + 1 + i * D_DX));
    }

    for (y = 0; y <= 14; y += 7) {
        for (x = 0; x <= 16; x += 8) {
            disphole(x, y, CHR_HOLE);
        }
    }

    while (in_KeyPressed(0x01bf) == 0) {     // press enter to exit
        if (in_KeyPressed(0x08ef) != 0) {
            disphole(0, 0, CHR_HAMMER_DSP);
        }
        else {
            disphole(0, 0, CHR_HAMMER_CLR);
        }

        in_Wait(10);    // wait(msec)
    }

    return (0);
}

